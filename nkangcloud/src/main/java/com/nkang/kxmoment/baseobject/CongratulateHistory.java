package com.nkang.kxmoment.baseobject;

public class CongratulateHistory {
	String num;
	String from;
	String to;
	String comments;
	String type;
	String point;
	String giftImg;
	String userImg;
	public String getUserImg() {
		return userImg;
	}
	public void setUserImg(String userImg) {
		this.userImg = userImg;
	}
	public String getGiftImg() {
		return giftImg;
	}
	public void setGiftImg(String giftImg) {
		this.giftImg = giftImg;
	}
	String congratulateDate;
	
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getCongratulateDate() {
		return congratulateDate;
	}
	public void setCongratulateDate(String congratulateDate) {
		this.congratulateDate = congratulateDate;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getPoint() {
		return point;
	}
	public void setPoint(String point) {
		this.point = point;
	}
	

}
